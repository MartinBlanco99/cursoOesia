package calculadora;

public class Calculadora {

	public double sumar(double a, double b) {
		return a + b;
	}
	
	public double restar(double a, double b) {
		return a - b;
	}
	
	public static double sumarNum(double a, double b) {
		return a + b;
	}

	public static double restarNum(double a, double b) {
		return a - b;
	}

}
